    public void mouseClicked(MouseEvent e)
    {
        System.gc();
        updateStatus();
        //System.out.println("GC");
        // if stopped - restart
        if(stopTimer == true)
        {
            stopTimer = false; 
            playTimer.start();
        }
    }
    

